						$('#tel').val(tel_txt);
						if(general.isTelPhone(tel_txt)){
							$('.popup-verity-btn').addClass("btn-effective");
						}else{
							$('.popup-verity-btn').removeClass("btn-effective");
						}
						general.alert('您已经注册过，请直接登录');
					}else{
						general.alert(ret.msg);
						general.timeoutFn && clearTimeout(general.timeoutFn);
						countDownCallback();
					}
				});
			break;
			case 'loginbtn':  //登录
				data={'Mobile':tel_txt,'ct_captcha':ver_txt};
				$.post('/trading_chain/trade/login',data,function(ret){
					if(ret.code==0){
						retdata=ret.data;
						RegisterAndLoginSuccess(retdata);
					}else if(ret.code==10002){  //未注册
						goRegister();
						$('#tel').val(tel_txt);
						if(general.isTelPhone(tel_txt)){
							$('.popup-verity-btn').addClass("btn-effective");
						}else{
							$('.popup-verity-btn').removeClass("btn-effective");
						}
						general.alert('您还未注册，请先注册');
					}else{
						general.alert(ret.msg);
						general.timeoutFn && clearTimeout(general.timeoutFn);
						countDownCallback();
					}
				});
			break;
		}
