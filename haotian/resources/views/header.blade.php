  		$('.popup-input-clear').hide();
  	}

    function CompatibleIEInput(){
      if(!('placeholder' in document.createElement('input'))){   //兼容IE8的placeholder 
          $('input[placeholder]').each(function(){ 
              var that = $(this),    
              text=that.attr('placeholder');   
              if(!that.val()){    
                that.val(text);    
              }
              that.focus(function(){    
                if(that.val()===text){    
                  that.val("");    
                }
              });
              that.blur(function(){    
                if(that.val()===""){    
                  that.val(text);    
                }    
              })
          });
        }
    }
