  	function goLogin(){  //去登录
      var userAgent=navigator.userAgent;
  		popupWindowInit();
  		$('#registertitle').hide();
  		$('#username').hide();
  		$('#registerbtn').hide();
  		$('#registertip').hide();
  		$('#logintitle').show();
  		$('#loginbtn').show();
  		$('#logintip').show();
      if(navigator.userAgent.indexOf('MSIE')>-1){  //兼容IE8
        $('.popup-window').removeClass('popup-window-dealregister-adj').addClass('popup-window-deallogin-adj');
        CompatibleIEInput();
      }else if(userAgent.toLowerCase().indexOf('iphone')>-1 || userAgent.toLowerCase().indexOf('android')>-1){  //手机端
        $('.popup-window').css('margin-top','0px');
        $('#tel').focus();
      }else{
  		  general.popupWindowVerticalCenter($('.popup-window')[0]);
  		  $('#tel').focus();
      }
  	}

  	function goRegister(){  //去注册
      var userAgent=navigator.userAgent;
      popupWindowInit();
      $('#logintitle').hide();
      $('#loginbtn').hide();
      $('#logintip').hide();
      $('#registertitle').show();
      $('#username').show();
      $('#registerbtn').show();
      $('#registertip').show();
      if(userAgent.indexOf('MSIE')>-1){
        $('.popup-window').removeClass('popup-window-deallogin-adj').addClass('popup-window-dealregister-adj');
        CompatibleIEInput();
      }else if(userAgent.toLowerCase().indexOf('iphone')>-1 || userAgent.toLowerCase().indexOf('android')>-1){  //手机端
        $('.popup-window').css('margin-top','0px');
        $('#username').val('').focus();
      }else{
        $('#username').val('').focus();
        general.popupWindowVerticalCenter($('.popup-window')[0]);
      }
  	}

