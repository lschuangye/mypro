			general.alert('请输入正确的验证码');
      $('#telverify').focus();
			return;
		}
		switch(id){
			case 'registerbtn':  //注册
				username_obj=$('#username');
				username=general.trim(username_obj.val());
				placeholder_nametxt=username_obj.attr('placeholder');
 				if((!('placeholder' in document.createElement('input')) &&username===placeholder_nametxt) || username===''){
					general.alert('请输入您的姓名');
					return;
				}
				data={'Name':username,'Mobile':tel_txt,'ct_captcha':ver_txt};
				$.post('/trading_chain/trade/register_in',data,function(ret){
